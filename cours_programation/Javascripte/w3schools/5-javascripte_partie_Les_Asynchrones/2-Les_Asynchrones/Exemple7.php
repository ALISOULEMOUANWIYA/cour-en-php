        <p> Une voiture est un véhicule à moteur autonome à roues utilisé pour le transport.
        La plupart des définitions du terme spécifient que les voitures sont conçues pour rouler principalement sur des routes, pour avoir des sièges pour une à huit personnes, pour avoir généralement quatre roues. </p>

